// THEME PROVIDER — Proveedor de tema (claro/oscuro)
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
// Usa next-themes para gestionar el cambio entre modo claro y oscuro.
// attribute="class" → agrega/elimina la clase "dark" en <html>
// defaultTheme="dark" → el tema por defecto es oscuro
// enableSystem → detecta la preferencia del sistema operativo
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
import { ThemeProvider as NextThemesProvider } from "next-themes";

export function ThemeProvider({
  children,
  ...props
}: React.ComponentProps<typeof NextThemesProvider>) {
  return <NextThemesProvider {...props}>{children}</NextThemesProvider>;
}
